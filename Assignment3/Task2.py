import math

num = float(input("Enter a number: "))

sqrt_val = math.sqrt(num)
log_val = math.log(num)
sine_val = math.sin(num)

print("Square root: ", num, "is:", sqrt_val)
print("Logarithm: ", num, "is:", log_val)
print("Sine: ", num, "is:", sine_val)
